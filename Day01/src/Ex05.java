
public class Ex05 {

	public static void main(String[] args) {
		
		boolean bool = true;
		byte by = 10;
		short sh = 11;
		char ch = 'A';
		int i = 100;
		long l = 1000L;
		float f = 3.1412f;
		double dou = 3.14;
		
			
		
		// 정수형 상수 -> 정수형 변수
		String str = "hello";
		
		System.out.println(str.length());
		
				
		 
		System.out.println(by);
		System.out.println(sh);
		System.out.println(i);
		System.out.println(l);
		// 정수형 상수 -> 실수형 변수
		f = 10.0f;
		dou = 11.0;
		System.out.println(f);
		System.out.println(dou);
		
	}

}
